package com.example.testingdm.charactercreation;

public class equipment {

    private String id;
    private String index;
    private String name;
    private String equipmentCat;
    private String weaponCat;
    private String weaponRng;
    private String categoryRng;

    private String currency;
    private int cost;

    private String dmgDice;
    private int dmgBonus;
    private String dmgType;

    private int range;
    private int weight;
    private String properties;

    public String getId() {
        return id;
    }

    public String getIndex() {
        return index;
    }

    public String getName() {
        return name;
    }

    public String getEquipmentCat() {
        return equipmentCat;
    }

    public String getWeaponCat() {
        return weaponCat;
    }

    public String getWeaponRng() {
        return weaponRng;
    }

    public String getCategoryRng() {
        return categoryRng;
    }

    public String getCurrency() {
        return currency;
    }

    public int getCost() {
        return cost;
    }

    public String getDmgDice() {
        return dmgDice;
    }

    public int getDmgBonus() {
        return dmgBonus;
    }

    public String getDmgType() {
        return dmgType;
    }

    public int getRange() {
        return range;
    }

    public int getWeight() {
        return weight;
    }

    public String getProperties() {
        return properties;
    }
}
